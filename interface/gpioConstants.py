# This file contains the GPIO definitions for the phone interface

# Upper threshold Pin for the phone line voltage
GPIO_LA_UPPER = 5
# Lower threshold Pin for the phone line voltage
GPIO_LA_LOWER = 6
# Heartbeat A LED Pin (reserved for the interface)
GPIO_HEARTBEAT_A = 23
# Heartbeat B LED Pin (reserved for the server)
GPIO_HEARTBEAT_B = 24
# On Hook LED Pin
GPIO_ON_HOOK = 8
# Off Hook LED Pin
GPIO_OFF_HOOK = 7
# Ring Relay Pin
GPIO_RING_RELAY = 12